def _sanitize_model_name(table_key: str) -> str:
    return "".join(ch if ch.isalnum() or ch == "_" else "_" for ch in table_key.lower())


def _schema_yml_for_model(model_name: str) -> str:
    return (
        "version: 2\n\n"
        "models:\n"
        f"  - name: {model_name}\n"
        "    description: Generated by AMA agentic extension\n"
        "    columns: []\n"
    )


def _build_normal_model_sql(
    table_key: str,
    mapped_columns: list[MappingRow],
    target_dialect: TargetDialect,
) -> str:
    if mapped_columns:
        projection = ",\n  ".join(
            f'"{row.hebrew_name}" AS {row.english_alias}' for row in mapped_columns
        )
    else:
        projection = "*"
    canonical_select = (
        "SELECT\n"
        f"  {projection}\n"
        f"FROM \"{table_key}\""
    )
    transpiled = transpile_sql(canonical_select, target_dialect)
    return "{{ config(materialized='view') }}\n\n" + transpiled


def _looks_like_select_sql(sql: str) -> bool:
    text = (sql or "").strip().lower()
    return "select" in text and "from" in text


def _contains_hebrew(value: str) -> bool:
    """Quick check for Hebrew codepoints used for deterministic translation rationale."""
    if not value:
        return False
    return bool(re.search(r"[\u0590-\u05FF]", value))


def _call_schema_agent(
    *,
    table_key: str,
    source_ddl_columns: list[str],
    raw_columns: list[str],
    ama_rationale: str,
) -> tuple[dict[str, Any], int, float]:
    user_prompt = json.dumps(
        {
            "table_key": table_key,
            "source_ddl_columns": source_ddl_columns,
            "sample_data_columns": raw_columns,
            "ama_report_rationale": ama_rationale,
            "response_schema": {
                "context_analysis": "string",
                "suggested_columns": ["string"],
                "confidence": "float_0_to_1",
            },
        },
        ensure_ascii=False,
    )
    result = query_openai_json(
        system_prompt=get_agent_prompt("schema_agent"),
        user_prompt=user_prompt,
        max_tokens=1200,
        timeout_seconds=45,
        temperature=0.0,
    )
    payload = result.payload
    confidence = float(payload.get("confidence") or 0.0)
    return payload, result.tokens_used, confidence


def _call_dbt_agent(
    *,
    table_key: str,
    schema_agent_output: dict[str, Any],
    mapped_columns: list[MappingRow],
    business_rationale: str,
    target_dialect: TargetDialect,
) -> tuple[str, str, int, float]:
    system_prompt = get_agent_prompt("dbt_agent").replace("{TARGET_DIALECT}", target_dialect.value)
    user_prompt = json.dumps(
        {
            "table_key": table_key,
            "schema_agent_output": schema_agent_output,
            "mapped_columns": [{"old": row.hebrew_name, "new": row.english_alias} for row in mapped_columns],
            "business_rationale": business_rationale,
            "target_dialect": target_dialect.value,
            "response_schema": {"sql": "string", "reasoning": "string", "confidence": "float_0_to_1"},
        },
        ensure_ascii=False,
    )
    result = query_openai_json(
        system_prompt=system_prompt,
        user_prompt=user_prompt,
        max_tokens=2500,
        timeout_seconds=60,
        temperature=0.1,
    )
    payload = result.payload
    sql = str(payload.get("sql") or "")
    reasoning = str(payload.get("reasoning") or "")
    confidence = float(payload.get("confidence") or 0.0)
    return sql, reasoning, result.tokens_used, confidence


